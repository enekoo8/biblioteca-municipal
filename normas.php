        <h2>Normas de convivencia</h2><br> 
        <p>Para fomentar un ambiente adecuado para la lectura, el trabajo y el estudio, es fundamental que todos los usuarios respeten y cumplan con una serie de normas básicas de convivencia. Estas reglas están establecidas con el propósito de garantizar un entorno tranquilo, ordenado y productivo, donde cada persona pueda concentrarse y aprovechar al máximo su tiempo. El incumplimiento de estas normas no solo afecta negativamente a quienes las infringen, sino también al resto de los usuarios, por lo que se aplicarán sanciones proporcionales a las faltas cometidas. En casos graves o recurrentes, se reserva el derecho de denegar la entrada al usuario en cuestión, con el fin de preservar el bienestar común.</p>
        <p>Estas normas están diseñadas para promover el respeto mutuo, la consideración hacia los demás y el uso responsable de los espacios compartidos. Entre las conductas que se esperan de los usuarios se incluyen mantener un tono de voz bajo, evitar distracciones innecesarias, respetar el mobiliario y las instalaciones, y abstenerse de realizar actividades que perturben la tranquilidad del lugar. Además, se recomienda apagar o silenciar dispositivos electrónicos para minimizar interrupciones.</p>
        <p>La colaboración de todos es esencial para mantener un ambiente agradable y propicio para el estudio y el trabajo. Por ello, se insta a los usuarios a ser conscientes de su comportamiento y a contribuir activamente a la armonía del espacio. En caso de observar incumplimientos de las normas, se puede informar al personal responsable para que tome las medidas correspondientes. Juntos, podemos asegurar que este lugar siga siendo un refugio de concentración, aprendizaje y productividad para todos.</p>
